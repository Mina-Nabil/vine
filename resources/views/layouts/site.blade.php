        function addToWishlist(id, elementID) {
            $("#" + elementID).attr("disabled", true);
            $.ajax({
                type: "POST",
                url: "{{$addToWishlistUrl}}",
                data: {
                    _token: "{{ csrf_token() }}",
                    id: id,
                },
                success: function (data) {
                    $("#" + elementID).removeAttr("disabled");
                    ret = JSON.parse(data);
                    if(ret.added==true){
                        showSuccessModal(ret.msg);
                    } else {
                        showErrorModal(ret.msg);
                    }
                },
                error: function (data){
                    $("#" + elementID).removeAttr("disabled");
                    showErrorModal(ret.msg);
                }
            });
        }

        function addToCart(id, quantity, size, color, elementID) {
            $("#" + elementID).attr("disabled", true);
            $.ajax({
                type: "POST",
                url: "{{$addToCartUrl}}",
                data: {
                    _token: "{{ csrf_token() }}",
                    id: id,
                    quantity: quantity,
                    size_id: size,
                    color_id: color
                },
                success: function (data) {
                    $("#" + elementID).removeAttr("disabled");
                    ret = JSON.parse(data);
                    if(ret.added==true){
                        populateHtmlTopbarCart(ret.cart);
                        showAddedProductModal(ret.product, ret.cart);
                    } else {
                        showErrorModal(ret.msg);
                    }
                },
                error: function (data){
                    $("#" + elementID).removeAttr("disabled");
                    showErrorModal();
                }
            });
        }


        function populateHtmlTopbarCart(cart) {
            $("#cart-content").html("");
            if(cart.items.length == 0){
                $('#cart-content').html(`
                    <div class="empty text-center"><em>Your shopping cart is empty. <a href="{{url('all')}}" class="btn btn-3">Continue Shopping</a></em></div>
                 `);
                $('.cartCount').html(0)
                return
            }
            $("#cart-content").append(` <div class="items control-container" id=cart-content-items>`);
            cart.items.forEach((item) => {
                $("#cart-content-items").append(
                    `<div class="row">
                        <a class="cart-close" title="Remove" id=nav-cart-remove-` +
                        item.id + ` href="javascript:void(0);" onclick="removeFromCart(` +
                        item.id + `, ` + item.color.id + `, ` + item.size.id + `,'nav-cart-remove-` +
                        item.id +
                        `' )"><i class="fa fa-times"></i></a>
                        <div class="cart-left">
                            <a class="cart-image" href="{{url('product')}}/` +
                        item.id +
                        `"><img src="` +
                        item.image_url +
                        `" alt="" title=""></a></div>
                        <div class="cart-right">
                            <div class="cart-title"><a href="{{url('product')}}/` +
                        item.id +
                        `">` +
                        item.title + ` - ` + item.variant +`</a></div>
                            <div class="cart-price"><span class="money" >` +
                        item.price +
